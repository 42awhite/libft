#include "libft.h"
#include <unistd.h>

void	ft_putstr_fd(char *s, int fd)
{
	int		cont;

	if (s != '\0')
	{
		cont = 0;
		while (s[cont])
		{
			write(fd, &s[cont], 1);
			cont++;
		}
	}
}
